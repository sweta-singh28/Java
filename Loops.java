class Loops {
    public static void main(String[] args){
        //loops
        //print 1 to 100
        for(int i=1; i<=100; i++){
            System.out.println(i);
        }
        
        System.out.println("Printing using while loop");

        int i=1;
        while(i<=100){
            System.out.println(i);
            i++;
        }
    }
    
}
